export enum TableColumnType {
    /**
     * A boolean (true / false)
     */
    BOOLEAN,
    /**
     * A 32-bit integer(-2147483648 - 2147483647)
     */
    INTEGER,
    /**
     * A 64-bit integer (-9223372036854775808 - 9223372036854775807)
     */
    LONG,
    /**
     * A double-precision 64-bit floating point number
     */
    DOUBLE,
    /**
     * A string
     */
    STRING
}